/**
 * Input data for a selected object in `ReferenceComponent`
 */
export interface ReferenceEntry {
  /**
   * id of a selected object
   */
  id: string;
  /**
   * ID of an object type
   */
  objectTypeId: string;
  /**
   * titel of a selected object
   */
  title: string;
  /**
   * description of a selected object
   */
  description: string;
}
